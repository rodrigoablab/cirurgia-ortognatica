                        <h2>A cirurgia ortognática é um procedimento necessário para corrigir e reposicionar os ossos da mandíbula e o posicionamento dentário de pacientes que apresentam diferentes graus de assimetria na região.</h2>

                        <figure>
                            <img src="images/cirurgia-ortognatica.jpg" alt="Cirurgia Ortognática">
                            <figcaption>O posicionamento inadequado da mandíbula podem prejudicar não só a aparência da região, mas também aspectos vitais, como o funcionamento das articulações empregadas na mordida, a saúde dos tecidos periodontais e até a função respiratória.</figcaption>
                        </figure>

                        <p>Mesmo se tratando de um procedimento cirúrgico que exige perícia e refinamento, a cirurgia ortognática traz resultados muito assertivos e seguros, tanto em relação às muitas melhorias na qualidade de vida do paciente quanto em sua autoestima.</p>

                        <p>A técnica proporciona modificações estéticas muito positivas para a harmonia da face. Uma das maiores vantagens dessa técnica é a ausência de cicatrizes, pois a intervenção é realizada na parte interior da boca.</p>
                    </div>

                    <div class="col-md-5">
                        <h2>Quando a cirurgia ortognática é recomendada?</h2>

                        <p>Geralmente, os fatores que desencadeiam anomalias do crescimento ósseo na região têm causas genéticas, e quando são acentuadas, acabam causando a desarmonia na estrutura do maxilar. Como se trata de um problema originado nos ossos, a sua correção precisa ir além do tratamento com aparelhos ortodônticos.</p>

                        <p>Por se tratar de uma solução definitiva para solucionar a assimetria óssea, a cirurgia ortognática requer um planejamento prévio cuidadoso por parte do dentista, do ortodontista e do paciente. É comum que o uso de aparelho ortodôntico anteceda o procedimento, pois o dispositivo auxilia no posicionamento estratégico dos dentes antes da realização do reposicionamento dos ossos da parte inferior da face.</p>

                        <p>A cirurgia ortognática também requer alguns cuidados especiais durante o período pós-operatório. Além de medicação específica, a recuperação total do paciente também envolve repouso, cuidados com a alimentação e fisioterapia.</p>

                        <p>Em alguns casos, também pode ser necessário que o paciente finalize o processo com a ajuda de tratamentos ortodônticos e outros procedimentos como implantes dentários e outros tratamentos estéticos. O resultado final é notável e trará melhorias perceptíveis para o seu rosto e sorriso.</p>
